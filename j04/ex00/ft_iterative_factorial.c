int		ft_iterative_factorial(int nb)
{
	int		y;

	y = nb;
	if ((nb < 0) || (nb > 12))
		return (0);
	if (nb >= 1)
	{
		while (nb > 1)
		{
			y = y * (nb - 1);
			nb--;
		}
		return (y);
	}
	else
		return (1);
}
