#include <stdio.h>

int		ft_sqrt(int nb)
{
	int i;

	i = 0;
	while (i * i != nb)
	{
		if (i * i > nb)
		{
			printf("0");
			return (0);
		}
		i++;
	}
	printf("%d", i);
	return (i);
}

int		main()
{
	int		nb;

	nb = 2000000000;
	ft_sqrt(nb);
	return(0);
}
