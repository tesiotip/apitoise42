int		ft_atoi(char *str)
{
	int y;
	int b;
	int nb;

	nb = 0;
	y = 0;
	b = 0;
	while (str[b] == ' ' || str[b] == '\t' || str[b] == '\n'
			|| str[b] == '\v' || str[b] == '\f' || str[b] == '\r')
		b++;
	if (str[b] == '-')
		y = 1;
	if (str[b] == '+' || str[b] == '-')
		b++;
	while ((str[b] >= '0' && str[b] <= '9') && (str[b] != '\0'))
	{
		nb = nb * 10;
		nb += str[b] - '0';
		b++;
	}
	if (y == 1)
		return (-nb);
	else
		return (nb);
}
